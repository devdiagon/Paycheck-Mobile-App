class AumentoRecord {
  final double salarioAnterior;
  final double salarioNuevo;
  final int antiguedadAnterior;
  final int antiguedadNueva;
  final DateTime fecha;
  final double aumento;

  AumentoRecord({
    required this.salarioAnterior,
    required this.salarioNuevo,
    required this.antiguedadAnterior,
    required this.antiguedadNueva,
    required this.fecha,
    required this.aumento,
  });
}